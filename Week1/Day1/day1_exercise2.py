# Định nghĩa các biến của các kiểu dữ liệu khác nhau
integer_var = 10
float_var = 3.14
string_var = "AI" # cũng có thể là 'AI'
list_var = [1,2,3]
tuple_var = (4,5,6)
dict_var = {"name": "Alice", "role": "Engineer"}
bool_var = True

# In và thao tác với các biến
print("Integer: ", integer_var)
print("Float: ", float_var)
print("String: ", string_var + " Bootcamp")
list_var.append(4) # Them 4 vao cuoi danh sach
print("List: ", list_var)
print("Tuple: ", tuple_var)
print("Dictionary Value:", dict_var["name"]) # xuất ra giá trị có key tên là name
print("Boolean: ", bool_var)